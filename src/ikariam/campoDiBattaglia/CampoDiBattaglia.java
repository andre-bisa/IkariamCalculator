package ikariam.campoDiBattaglia;

public class CampoDiBattaglia {
	
	public final int numeroBombardieri;
	public final int dimensioneBombardieri;
	
	public final int numeroArtiglieria;
	public final int dimensioneArtiglieria;
	
	public final int numeroDistanza;
	public final int dimensioneDistanza;
	
	public final int numeroPrimaLinea;
	public final int dimensionePrimaLinea;
	
	public final int numeroFianco;
	public final int dimensioneFianco;
	
	public final int numeroContraerea;
	public final int dimensioneContraerea;
	
	public CampoDiBattaglia(int numeroBombardieri, int dimensioneBombardieri, int numeroArtiglieria,
			int dimensioneArtiglieria, int numeroDistanza, int dimensioneDistanza, int numeroPrimaLinea,
			int dimensionePrimaLinea, int numeroFianco, int dimensioneFianco, int numeroContraerea,
			int dimensioneContraerea) {
		this.numeroBombardieri = numeroBombardieri;
		this.dimensioneBombardieri = dimensioneBombardieri;
		this.numeroArtiglieria = numeroArtiglieria;
		this.dimensioneArtiglieria = dimensioneArtiglieria;
		this.numeroDistanza = numeroDistanza;
		this.dimensioneDistanza = dimensioneDistanza;
		this.numeroPrimaLinea = numeroPrimaLinea;
		this.dimensionePrimaLinea = dimensionePrimaLinea;
		this.numeroFianco = numeroFianco;
		this.dimensioneFianco = dimensioneFianco;
		this.numeroContraerea = numeroContraerea;
		this.dimensioneContraerea = dimensioneContraerea;
	}
	
}
